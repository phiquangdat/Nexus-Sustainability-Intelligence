-- Function to update updated_at timestamp
create or replace function update_updated_at_column()
returns trigger as $$
begin
    new.updated_at = now();
    return new;
end;
$$ language plpgsql;

-- Triggers for updated_at
create trigger update_power_plants_updated_at before update on public.power_plants
    for each row execute function update_updated_at_column();

create trigger update_eu_ets_reports_updated_at before update on public.eu_ets_reports
    for each row execute function update_updated_at_column();

create trigger update_netzero_alignment_updated_at before update on public.netzero_alignment
    for each row execute function update_updated_at_column();

