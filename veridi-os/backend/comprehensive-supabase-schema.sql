-- Comprehensive Supabase Schema for Veridi OS Sustainability Intelligence Platform
-- This schema integrates both the original power plant data and the new sustainability intelligence tables

-- ============================================================================
-- ORIGINAL POWER PLANT DATA TABLES
-- ============================================================================

-- Power plants table
create table if not exists public.power_plants (
    id bigint generated by default as identity primary key,
    plant_name text not null,
    plant_type text not null,
    capacity_mw numeric not null,
    location text,
    country text,
    commissioning_date date,
    created_at timestamptz default now(),
    updated_at timestamptz default now()
);

-- Power plant emissions data
create table if not exists public.power_plant_emissions (
    id bigint generated by default as identity primary key,
    plant_id bigint references public.power_plants(id) on delete cascade,
    timestamp timestamptz not null,
    co2_emissions_tonnes numeric not null,
    power_output_mw numeric not null,
    efficiency_pct numeric,
    fuel_type text,
    created_at timestamptz default now()
);

-- EU ETS reports
create table if not exists public.eu_ets_reports (
    id bigint generated by default as identity primary key,
    reporting_period text not null,
    start_date date not null,
    end_date date not null,
    total_emissions_tonnes numeric not null,
    status text not null default 'draft',
    generated_at timestamptz default now(),
    created_at timestamptz default now(),
    updated_at timestamptz default now()
);

-- ============================================================================
-- SUSTAINABILITY INTELLIGENCE TABLES
-- ============================================================================

-- CO2 intensity (gCO2/kWh) time-series
create table if not exists public.co2_intensity (
    id bigint generated by default as identity primary key,
    timestamp timestamptz not null,
    co2_intensity_g_per_kwh numeric not null,
    created_at timestamptz default now()
);

-- Generation mix (MW) by technology with renewable share
create table if not exists public.generation_mix (
    id bigint generated by default as identity primary key,
    timestamp timestamptz not null,
    hydro_mw numeric not null,
    wind_mw numeric not null,
    solar_mw numeric not null,
    nuclear_mw numeric not null,
    fossil_mw numeric not null,
    total_mw numeric not null,
    renewable_share_pct numeric not null,
    created_at timestamptz default now()
);

-- Net-zero trajectory alignment per year
create table if not exists public.netzero_alignment (
    year int primary key,
    actual_emissions_mt numeric not null,
    target_emissions_mt numeric not null,
    alignment_pct numeric not null,
    created_at timestamptz default now(),
    updated_at timestamptz default now()
);

