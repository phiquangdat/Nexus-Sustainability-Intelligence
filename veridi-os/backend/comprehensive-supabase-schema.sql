-- View for latest sustainability metrics
create or replace view public.latest_sustainability_metrics as
select 
    (select co2_intensity_g_per_kwh from public.co2_intensity order by timestamp desc limit 1) as latest_co2_intensity,
    (select renewable_share_pct from public.generation_mix order by timestamp desc limit 1) as latest_renewable_share,
    (select alignment_pct from public.netzero_alignment order by year desc limit 1) as latest_netzero_alignment,
    (select count(*) from public.co2_intensity) as co2_data_points,
    (select count(*) from public.generation_mix) as generation_data_points,
    (select count(*) from public.netzero_alignment) as netzero_data_points;

-- View for power plant summary
create or replace view public.power_plant_summary as
select 
    pp.id,
    pp.plant_name,
    pp.plant_type,
    pp.capacity_mw,
    pp.location,
    pp.country,
    count(ppe.id) as emission_records_count,
    sum(ppe.co2_emissions_tonnes) as total_emissions_tonnes,
    avg(ppe.efficiency_pct) as avg_efficiency_pct,
    max(ppe.timestamp) as last_emission_record
from public.power_plants pp
left join public.power_plant_emissions ppe on pp.id = ppe.plant_id
group by pp.id, pp.plant_name, pp.plant_type, pp.capacity_mw, pp.location, pp.country;

